// Helper function to transform text using a character map
const transformWithMap = (text: string, map: Record<string, string>): string => {
  return [...text].map((char) => map[char] || char).join("")
}

// Stylish fonts generator
export const generateStylishFonts = (text: string): string[] => {
  return [
    transformWithMap(text, stylishFont1), // Script
    transformWithMap(text, stylishFont2), // Bold Script
    transformWithMap(text, stylishFont3), // Double-struck
    transformWithMap(text, stylishFont4), // Bold Fraktur
    transformWithMap(text, stylishFont5), // Sans-serif
    transformWithMap(text, stylishFont6), // Small caps
    transformWithMap(text, stylishFont7), // Squared
    transformWithMap(text, stylishFont8), // Circled
  ]
}

// Facebook fonts generator (optimized for Facebook)
export const generateFacebookFonts = (text: string): string[] => {
  return [
    transformWithMap(text, stylishFont2), // Bold Script - works well on Facebook
    transformWithMap(text, stylishFont3), // Double-struck - good visibility
    transformWithMap(text, stylishFont6), // Small caps - professional look
    transformWithMap(text, stylishFont8), // Circled - fun for posts
    transformWithMap(text, stylishFont1), // Script - elegant
    // Add some creative combinations
    [...text]
      .map((char, i) => (i % 2 === 0 ? char.toUpperCase() : char.toLowerCase()))
      .join(""), // Alternating case
    text
      .split("")
      .join(" "), // Spaced out
    text
      .toUpperCase()
      .split("")
      .join(" "), // Uppercase spaced
  ]
}

// Instagram fonts generator (optimized for Instagram)
export const generateIGFonts = (text: string): string[] => {
  return [
    transformWithMap(text, stylishFont2), // Bold Script - Instagram favorite
    transformWithMap(text, stylishFont1), // Script - elegant for bios
    transformWithMap(text, stylishFont8), // Circled - trendy
    transformWithMap(text, stylishFont3), // Double-struck - stands out
    transformWithMap(text, stylishFont6), // Small caps - modern
    transformWithMap(text, stylishFont7), // Squared - bold
    // Instagram-specific styles
    text
      .split("")
      .map((char) => char + "Ì²")
      .join(""), // Underlined effect
    text
      .split("")
      .map((char) => char + "Ìƒ")
      .join(""), // Tilde effect
    [...text]
      .map((char, i) => (i % 2 === 0 ? transformWithMap(char, stylishFont2) : transformWithMap(char, stylishFont1)))
      .join(""), // Mixed styles
  ]
}
