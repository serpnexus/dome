// Helper function
const transformWithMap = (text: string, map: Record<string, string>): string => {
  return [...text].map((char) => map[char] || char).join("")
}

// Fantasy fonts generator
export const generateFantasyFonts = (text: string): string[] => {
  return [
    transformWithMap(text, fantasyFont1), // Gothic/Fraktur
    transformWithMap(text, fantasyFont2), // Bold Fraktur
    text
      .split("")
      .map((char) => char + "âƒ")
      .join(""), // Squared overlay
    text
      .split("")
      .map((char) => char + "âƒŸ")
      .join(""), // Double struck overlay
    text.replace(/./g, (char, i) => (i % 2 === 0 ? char.toUpperCase() : char.toLowerCase())), // Alternating case
    text
      .split("")
      .join(" "), // Spaced
    "âœ¦" + text.split("").join("âœ¦") + "âœ¦", // Star separated
    "â—†" + text + "â—†", // Diamond wrapped
  ]
}

